def reverse(string): 
    string = string[::-1]
    return string

# m: number of rows, n: number of columns
def create_matrix(m, n):
    return [[0]*n for _ in range(m)]

def match_score(alpha, beta):
    return Pam250[alpha][beta]

def calAlignment(A, B, L, oploc):
    # Retrieve the location of highest score
    i, j = oploc
    
    # Final strings
    FinalA = ""
    FinalB = ""
    # Check the alignment
    while L[i][j] > 0:
        if L[i][j] == 3:
            FinalA += A[i-1]
            FinalB += B[j-1]
            i -= 1
            j -= 1
        else:
            if L[i][j] == 1:
                FinalA += '-'
                FinalB += B[j-1]
                j -= 1
            else:
                FinalA += A[i-1]
                FinalB += '-'
                i -= 1
                
    return reverse(FinalA), reverse(FinalB)
        

def FindMaxScore(A, B, delta):
    # To track the best score
    best = 0
    # To track optimal local alignment
    optloc = (0, 0)
    # Create F matrix for scoring
    F = create_matrix(len(A) + 1, len(B) + 1)
    # Create L matrix for locating
    L = create_matrix(len(A) + 1, len(B) + 1)
   
    # Fill in matrix F, L in the right order
    for i in range(1, len(A)+1):
        for j in range(1, len(B)+1):
            #String1 delete
            ADel = F[i][j-1] + delta
            #String2 delete
            BDel = F[i-1][j] + delta
            #Diagonal Score
            Diagonal = F[i-1][j-1] + match_score(A[i-1], B[j-1])
            # The locl alignment recurrence rule;
            Maxscore = max(ADel, BDel, Diagonal, 0)
            F[i][j] = Maxscore
            
            #Set L matrix
            if ADel == Maxscore:
                L[i][j] = 1 #1 == "left"
            elif BDel == Maxscore:
                L[i][j] = 2 #2 == "up"
            elif Diagonal == Maxscore:
                L[i][j] = 3 #3 == "diagonal"
            
            # Track the cell with the largest score
            if F[i][j] >= best:
                best = Maxscore
                optloc = (i, j)
            
    # Return the opt score and the best location
    return L, best, optloc

